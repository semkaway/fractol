int			ft_atoi(const char *str)
{
	int				i;
	int				j;
	int				k;

	i = 0;
	k = 0;
	j = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == '\n')
		i++;
	if (str[i] == '+' || str[i] == '-')
		i++;
	if (str[i - 1] == '-')
		j = 1;
	while (str[i] >= '0' && (str[i] <= '9'))
	{
		k *= 10;
		k += (int)str[i] - '0';
		i++;
	}
	if (j == 1)
		return (-k);
	return (k);
}
