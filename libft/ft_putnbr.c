#include "includes/libft.h"

void	ft_putnbr(int n)
{
	long long	k;
	long long	j;

	k = n;
	if (k < 0)
	{
		k = -k;
		write(1, "-", 1);
	}
	if (k >= 10)
	{
		ft_putnbr(k / 10);
		ft_putnbr(k % 10);
	}
	else
	{
		j = k + '0';
		write(1, &j, 1);
	}
}
