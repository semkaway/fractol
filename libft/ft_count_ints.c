int				ft_count_ints(int i)
{
	int			count;
	long long	k;

	count = 1;
	k = i;
	if (k < 0)
	{
		k *= -1;
		count++;
	}
	if (k >= 0 && k <= 9)
		return (count);
	while (k > 9)
	{
		k = k / 10;
		count++;
	}
	return (count);
}
