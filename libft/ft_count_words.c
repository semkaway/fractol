int			ft_count_words(const char *s, char c)
{
	int		i;
	int		words;

	i = 0;
	words = 0;
	if (s[0] == '\0')
		return (words);
	if (s[i] != c)
	{
		words++;
		i++;
	}
	while (s[i] == c && s[i + 1] == c)
		i++;
	while (s[i] != '\0')
	{
		if ((s[i] == c && s[i + 1] != c) && (s[i] == c && s[i + 1] != '\0'))
			words++;
		i++;
	}
	return (words);
}
